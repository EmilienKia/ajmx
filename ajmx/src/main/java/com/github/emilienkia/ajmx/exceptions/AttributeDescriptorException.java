package com.github.emilienkia.ajmx.exceptions;

public class AttributeDescriptorException extends AJMXException {

    public AttributeDescriptorException() {
    }

    public AttributeDescriptorException(String message) {
        super(message);
    }

    public AttributeDescriptorException(String message, Throwable cause) {
        super(message, cause);
    }

    public AttributeDescriptorException(Throwable cause) {
        super(cause);
    }
}
